# 3

# TODO aの各要素を2倍した配列bを作ってください
b = []

for x in a:
    b.append(x * 2)

assert(b == [6, 8, 10])

# TODO aとbの和cを計算してください
# 和は、正の整数のnに対して
# c[n]= a[n] + b[n]
# と計算します
c = []

for i in range(len(a)):
    c.append(a[i] + b[i])

assert(c == [ 9, 12, 15])

# TODO 行列dと行列eの和fを計算してください
# 和は、正の整数i, jに対して
# f[i][j] = d[i][j] + e[i][j]
# と計算します
d = [
  [19,  8, 25],
  [44, 11,  9]
]

e = [
  [ 5,  8,  5],
  [34, 17,  0]
]

f = []
for i in range(len(d)):
    k = []
    f.append(k)
    for j in range(len(d[0])):
        k.append(d[i][j] + e[i][j])

assert(f == [
  [24, 16, 30],
  [78, 28,  9]
])

