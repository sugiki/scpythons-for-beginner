# 配列を用意します
a = [3, 4, 5]

# このとき、配列に対しforを使うことができます
for x in a:
    print(x)

# プログラムとして
# 3
# 4
# 5
# と表示されるはずです

# また、今回の問題で1からnまでの整数の繰り返しを作る必要があるときは
