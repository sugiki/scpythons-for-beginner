# 行頭に # を書くと、プログラムとして処理されない文字列（コメントと呼ばれます）を書くことができます

# 以下の例にならって、文字を表示してください
# ex. 1 + 1 の結果を表示する
print(1 + 1)

# 1. 10 + 7 の結果を表示する
print('Addition')
print(10 + 7)
# 2. 10 - 7 の結果を表示する
print('Subtraction')
print(10 - 7)
# 3. 10 * 7 の結果を表示する
print('Multiplication')
print(10 * 7)
# 3. 10 / 7 の結果を表示する
print('Division')
print(10 / 7)
# 4. 10 % 7 の結果を表示する
print('Modulus')
print(10 % 7)
# 5. 10 ** 7 の結果を表示する
print('Exponent')
print(10 ** 7)
# 6. 10 // 7 の結果を表示する
print('Floor Division')
print(10 // 7)
